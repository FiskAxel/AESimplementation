        static int[][] MessageToBlocks(string message, char padding)
        {
            decimal len = Math.Ceiling((decimal)message.Length / 16);
            int[][] blocks = new int[(int)len][];
            for (int i = 0; i < len; i++)
            {
                int[] newBlock = new int[16];
                for (int j = 0; j < 16; j++)
                {
                    if ((i * 16 + j) >= message.Length)
                    {
                        // Adds padding to last block if needed.
                        newBlock[j] += padding;
                    }
                    else
                    {
                        newBlock[j] = ((int)message[i * 16 + j]);
                    }
                }
                blocks[i] = newBlock;
            }

            return blocks;
        }
        static string DecryptedBlocksToString(int[][] blocks, char padding)
        {
            string decrypted = "";
            // Adding all blocks, except the last, to the string.
            for (int i = 0; i < blocks.Length - 1; i++)
            {
                foreach (int c in blocks[i])
                {
                    decrypted += (char)c;
                }
            }
            // Unpadding last block and adding to the string.
            int len = GetIndexOfPadding(blocks[blocks.Length - 1], padding);
            for (int j = 0; j < len; j++)
            {
                decrypted += (char)blocks[blocks.Length - 1][j];
            }
            return decrypted;
        }
        static int GetIndexOfPadding(int[] lastBlock, char padding) 
        {
            // With this function its possible for the last block of the message to contain the padding element
            // without obscuring the message (as long as the message doesn't contain the padding element at the very end)

            if (lastBlock[15] != padding)
            {
                return 16;
            }

            int index = 0;
            for (int i = 1; i < 16; i++)
            {
                index = i;
                while(i < 15 &&
                      lastBlock[i] == (int)padding && 
                      lastBlock[i + 1] == (int)padding)
                {
                    i++;
                }
            }
            return index;
        }

        static int[][] KeyExpansion(string inputKey, int rounds, int[] sBox)
        {
            // 11 keys are needed for 128-bit AES.
            int[][] keys = new int[11][];
            keys[0] = new int[16];
            // The key for round zero is the inputted key.
            for (int i = 0; i < inputKey.Length; i++)
            {
                // Converts character to unicode. 
                keys[0][i] = (int)inputKey[i];
                // Program doesnt crash if user inputs key longer than 128-bit.
                if (i == 15) { break; }
            }

            
            for (int i = 0; i < rounds; i++)
            {
                int[] newKey = new int[16];
                KeyExpansionFirstCol(newKey, keys[i], i + 1, sBox);

                // Coullumns 2, 3 and 4
                for (int j = 4; j < 16; j++)
                {
                    // "Word" i = ("Word" i - 1) XOR ("Word" i - 4)
                    newKey[j] = keys[i][j] ^ newKey[j - 4];
                }

                keys[i + 1] = newKey;
            }

            return keys;
        }
        static void KeyExpansionFirstCol(int[] newKey, int[] prevKey, int i, int[] sBox)
        {
            // Get last collumn of previous key ("Word" i - 1)
            newKey[0] = prevKey[12];
            newKey[1] = prevKey[13];
            newKey[2] = prevKey[14];
            newKey[3] = prevKey[15];

            // Rotate
            int temp = newKey[0];
            newKey[0] = newKey[1];
            newKey[1] = newKey[2];
            newKey[2] = newKey[3];
            newKey[3] = temp;

            // SubBytes
            newKey[0] = sBox[newKey[0]];
            newKey[1] = sBox[newKey[1]];
            newKey[2] = sBox[newKey[2]];
            newKey[3] = sBox[newKey[3]];

            // Rcon (not the full table)
            int[] rcon = { 0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };
            newKey[0] ^= rcon[i];

            // XOR by "Word" i - 4;
            newKey[0] ^= prevKey[0];
            newKey[1] ^= prevKey[1];
            newKey[2] ^= prevKey[2];
            newKey[3] ^= prevKey[3];
        }

        static void AddRoundKey(int[] state, int[] roundKey)
        {
            // Each byte in the state is XOR-ed by the byte in roundKey with the same index.
            for (int i = 0; i < 16; i++)
            {
                state[i] ^= roundKey[i];
            }
        }
        static void SubBytes(int[] state, int[] lookupTable)
        { 
            // The new value for each byte is fetched from a lookup table.
            for (int i = 0; i < 16; i++)
            {
                state[i] = lookupTable[state[i]];
            }
        }
